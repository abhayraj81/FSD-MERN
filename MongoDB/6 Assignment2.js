//   3) Find employees whose name starts with S, sort them by salary in descending order, and 
//   display only the first 5 records. 
//   4) Find employees whose email ends with gmail.com (case-insensitive), skip the first 3 matching 
//   documents, and display the next 5. 
//   5) Update all employees whose designation contains the word Manager (case-insensitive) by setting 
//   bonusEligible to true.


//_______________________ANSWERS__________________________________________

//1)  Update all employees whose name starts with A, set department to "IT" and status to "Active" 
db.assignment.updateMany({name : /^a/i},
                          {$set:{department:"IT",status:'Active'}}
                          )

//Rename the field mobile to phone, remove the tempAddress field, and update updatedAt with the current date for employees whose city is "Delhi".
db.assignment.updateMany({city:{$eq:"Delhi"}},
                         {$rename:{'moible':'phone'},
                         $unset:{'tempAddress':''},
                         $set: {'updatedAt': new Date}}
                        )

//3. Find employees whose name starts with S, sort them by salary in descending order, and display only the first 5 records. 
